//Reverse array
public class Infosys5 {

	public static void main(String[] args) {
		int[]arr= {1,2,3,4,5};
		for(int i =arr.length-1;i>=0;i--) {
			//i=5-1=4
			//i=4
			//arr[4]=5
			//arr[3]=4
			//arr[2]=3
			//arr[1]=2
			//arr[0]=1
			System.out.println(arr[i]);

	}
	}

}
